document.querySelector('.text-1-click').addEventListener('click', function() {
  var timelineGeneralInfo = document.querySelector('.timeline-general-info');
  for (var i = 0; i < timelineGeneralInfo.classList.length; i++) {
    if (timelineGeneralInfo.classList[i] !== 'timeline-general-info') {
      timelineGeneralInfo.classList.remove(timelineGeneralInfo.classList[i]);
    }
  }
  timelineGeneralInfo.classList.add('text-1-show');
});

